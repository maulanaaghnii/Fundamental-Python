#NUMBER

# Di python tidak hanya ada TEKS , namun juga ada NUMBER, perbedaannya adalah 

print("5") # ini adalah TEKS 

print(5) # ini adalah NUMBER

# Sekilas tidak terlihat perbedaannya, namun jika TEKS walaupun berbentuk angka, tetapi TEKS tidak dapat dioperasikan menggunakan operasi matematika


# Terdapat beberapa jenis number di Python
# Diantaranya yang paling populer adalah Integer dan Float

# Integer adalah bilangan Bulat
print("Ini adalah Integer : ")
print(1)
print(2)
print(3)
print(100)
print(1000)
print(1000000)


# Float adalah bilangan rasional atau bilangan koma
print("Ini adalah Float : ")
print(1.5)
print(3.14)
print(5.0)